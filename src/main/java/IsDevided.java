public class IsDevided {
    public static void main(String[] args) {
        int i = 30;

        if (i % 3 == 0 && i % 5 == 0) {
            System.out.println("This number can be divided by 3 and 5.");
        }
        else {
            System.out.println("This number cannot be divided by 3 and 5.");
        }
    }
}
